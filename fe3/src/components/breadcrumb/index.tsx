export const Breadcrumb = ({ first = 'Home', middle = '', last = '', onChange = () => {} }) => {
  let firstIcon = <Home />;
  let firstLink = '/imls';
  if (first === 'Explore') {
    firstIcon = <Explore />;
    firstLink = '/imls/explore-oer-exchange';
  } else if (first === 'Library') {
    firstIcon = <MyLibrary />;
    firstLink = '/imls/site-collections/main';
  }
  let middleIcon;
  let middleLink = '';
  if (middle === 'Subscribed Content') {
    middleIcon = <Subscribed />;
    middleLink = '/imls/site-collections/subscribed-collections';
  } else if (middle === 'Shared Content') {
    middleIcon = <Shared />;
    middleLink = '/imls/site-collections/shared-collections';
  } else if (middle === 'All Collections') {
    middleLink = '/imls/browse';
  } else if (middle === 'Search OER') {
    middleLink = '/imls/search';
  }
  let lastIcon;
  if (last === 'Updates') lastIcon = <Updates />;
  else if (last === 'Preferences') lastIcon = <Preferences />;
  else if (last === 'Search') lastIcon = <Search />;
  return (
    <div className={cls['frame']}>
      <div className={cls['div']}>
        <div className={cls['frame-wrapper']}>
          <Link to={firstLink} onClick={onChange}>
            <div className={cls['div']}>
              {firstIcon}
              <div className={cls['sort-by']}>{first}</div>
            </div>
          </Link>
        </div>
        {/* <Arrow /> */}
      </div>
      {middle && (
        <>
          <Divider />
          <Link to={middleLink} onClick={onChange}>
            <div className={cls['subscribed-content']}>
              {middleIcon}
              <div className={cls['text']}>{middle}</div>
            </div>
          </Link>
        </>
      )}
      {last && (
        <>
          <Divider />
          <div className={cls['subscribed-content']}>
            {lastIcon}
            <div className={cls['text']}>{last}</div>
          </div>
        </>
      )}
    </div>
  );
};
